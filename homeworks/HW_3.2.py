# --- Приклад 1 ---
list_1 = [12, 3, 4, 10]
print(f"Початковий список: {list_1}")

# Перевіряємо, чи список містить 2 або більше елементів
if len(list_1) >= 2:
    # 1. З методом .pop() видаляємо останній елемент та повертає його
    last_element = list_1.pop()
    # 2. З методом .insert(0, ...) вставляємо цей елемент на першу позицію
    list_1.insert(0, last_element)

print(f"Кінцевий список:   {list_1}")
print("-" * 20) # Роздільник для наочності


# --- Приклад 2 ---
list_2 = [1]
print(f"Початковий список: {list_2}")

# Умова len(list_2) >= 2 буде хибною (False), тому блок коду не виконається
if len(list_2) >= 2:
    last_element = list_2.pop()
    list_2.insert(0, last_element)

print(f"Кінцевий список:   {list_2}")
print("-" * 20) # Роздільник для наочності


