# Конструкція `while True:` запускає цикл, який буде повторюватися вічно,
# доки ми не вийдемо з нього примусово за допомогою команди `break`.
while True:
    # Запитуємо перше число.
    number_1_str = input("Введіть перше число: ")
    # Запитуємо друге число.
    number_2_str = input("Введіть друге число: ")
    # Запитуємо, яку дію потрібно виконати.
    operation = input("Введіть дію (+, -, *, /): ")

    # Перетворюємо рядки на дробовий тип `float`.
    number_1 = float(number_1_str)
    number_2 = float(number_2_str)

    # Перевіряємо, який символ операції ввів користувач.
    if operation == '+':
        result = number_1 + number_2
        print("Результат:", result)
    elif operation == '-':
        result = number_1 - number_2
        print("Результат:", result)
    elif operation == '*':
        result = number_1 * number_2
        print("Результат:", result)
    elif operation == '/':
        if number_2 == 0:
            print("Помилка! Ділення на нуль неможливе.")
        else:
            result = number_1 / number_2
            print("Результат:", result)
    else:
        print("Помилка! Введено невірну дію.")

    # Далі нижче запит на продовження та аналіз відповіді

    print("-" * 20)  # Роздільник для кращої читабельності

    # Запитуємо користувача, чи хоче він продовжити.
    answer = input("Хочете продовжити? (yes/no): ")

    # Перетворюємо відповідь у нижній регістр за допомогою методу .lower()
    # Це дозволяє нам не перевіряти окремо "YES", "Yes", "Y" і т.д.
    # Якщо відповідь НЕ "yes" і НЕ "y", то виходимо з циклу.
    if answer.lower() != 'yes' and answer.lower() != 'y':
        # Команда break негайно перериває цикл while.
        break

# Цей рядок виконається тільки після того, як цикл буде завершено.
print("Дякуємо за використання калькулятора. До побачення!")