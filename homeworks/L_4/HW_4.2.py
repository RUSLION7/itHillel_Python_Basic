my_list = [0, 1, 7, 2, 4, 8]

print(f"Початковий список: {my_list}")

# Перевіряємо, чи є у списку елементи.
if not my_list:
    result = 0
else:
    # Створюємо змінну для накопичення суми.
    sum_even_indices = 0

    # Використовуємо цикл for з функцією enumerate, щоб отримати
    # одночасно індекс (index) та значення (element) на кожному кроці.
    for index, element in enumerate(my_list):
        # Перевіряємо, чи є індекс парним.
        # Залишок від ділення на 2 для парних чисел завжди дорівнює 0.
        if index % 2 == 0:
            # Якщо індекс парний, додаємо значення елемента до нашої суми.
            sum_even_indices += element

    # Отримуємо останній елемент списку за допомогою негативного індексу [-1].
    last_element = my_list[-1]

    # Перемножуємо суму на останній елемент.
    result = sum_even_indices * last_element

# Виводимо результат
print(f"Результат: {result}")