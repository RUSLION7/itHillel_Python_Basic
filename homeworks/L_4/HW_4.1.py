my_list = [1, 0, 13, 0, 0, 0, 5]

# 1. Створюємо два порожні списки для сортування елементів.
non_zeros = []
zeros = []

print(f"Початковий список: {my_list}")

# 2. Перебираємо кожен елемент у вихідному списку.
for element in my_list:
    # 3. Перевіряємо, чи є елемент нулем.
    if element == 0:
        # Якщо так, додаємо його до списку нулів.
        zeros.append(element)
    else:
        # Якщо ні, додаємо його до списку ненульових чисел.
        non_zeros.append(element)

# 4. Об'єднуємо два списки. Порядок ненульових елементів зберігається.
result_list = non_zeros + zeros

# --- Виведення результату ---
print(f"Кінцевий список:   {result_list}")