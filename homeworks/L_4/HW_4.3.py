# Імпортуємо модуль random.
import random

# Визначаємо випадкову кількість елементів для нашого списку (від 3 до 10 включно).
list_size = random.randint(3, 10)

# Створюємо порожній список, який будемо наповнювати.
random_list = []

# За допомогою циклу for наповнюємо список випадковими числами.
# Цикл виконається 'list_size' разів.
for _ in range(list_size):
    # На кожному кроці генеруємо випадкове ціле число (наприклад, від 1 до 100)
    # і додаємо його в кінець списку.
    random_list.append(random.randint(1, 100))

print(f"Згенерований випадковий список: {random_list}")


# Використовуємо індекси для доступу до потрібних елементів:
# Перший елемент має індекс 0.
first_element = random_list[0]
# Третій елемент має індекс 2.
third_element = random_list[2]
# Другий елемент з кінця має негативний індекс -2.
second_to_last_element = random_list[-2]

# Створюємо новий список, що складається з цих трьох елементів.
result_list = [first_element, third_element, second_to_last_element]

# --- Виведення результату ---
print(f"Фінальний список з 3-х елементів: {result_list}")