# Отримуємо число від користувача.
number = int(input("Введіть ціле число: "))

# Зовнішній цикл: повторюємо, поки число не стане однозначним.
while number > 9:
    # Ініціалізуємо добуток для поточної ітерації.
    product = 1
    # Перебираємо цифри поточного числа, перетворивши його на рядок.
    for digit_char in str(number):
        # Перемножуємо цифри.
        product *= int(digit_char)
    # Оновлюємо число для наступної ітерації.
    number = product

# Виводимо кінцевий результат (число <= 9).
print(number)