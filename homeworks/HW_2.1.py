# Запитуємо у користувача чотиризначне число і перетворюємо його на цілочисельний тип
number = int(input("Введите четырехзначное число: "))

# Видобуваємо першу цифру (тисячі), використовуючи цілочисельне ділення
# Приклад: 1234 // 1000 = 1
digit1 = number // 1000

# Видобуваємо другу цифру (сотні).
# Спочатку отримуємо перші дві цифри: 1234 // 100 = 12
# Потім беремо останню цифру від результату: 12 % 10 = 2
digit2 = (number // 100) % 10

# Видобуваємо третю цифру (десятки).
# Спочатку отримуємо перші три цифри: 1234 // 10 = 123
# Потім беремо останню цифру від результату: 123 % 10 = 3
digit3 = (number // 10) % 10

# Видобуваємо четверту цифру (одиниці), використовуючи залишок від ділення
# Приклад: 1234 % 10 = 4
digit4 = number % 10

# Послідовно виводимо кожну знайдену цифру
print(digit1)
print(digit2)
print(digit3)
print(digit4)