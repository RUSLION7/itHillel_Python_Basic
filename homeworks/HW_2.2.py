# Запитуємо у користувача п'ятизначне додатне число
number = int(input("Введіть п'ятизначне додатне число: "))

# Видобуваємо першу цифру (розряд десятків тисяч)
# Приклад: 12345 // 10000 -> 1
digit1 = number // 10000

# Видобуваємо другу цифру (розряд тисяч)
# Спочатку отримуємо перші дві цифри (12345 // 1000 -> 12)
# потім беремо залишок від ділення на 10, щоб отримати останню з них (12 % 10 -> 2)
digit2 = (number // 1000) % 10

# Видобуваємо третю цифру (розряд сотень)
# Аналогічно, отримуємо перші три цифри (12345 // 100 -> 123)
# і беремо останню з них (123 % 10 -> 3)
digit3 = (number // 100) % 10

# Видобуваємо четверту цифру (розряд десятків)
# Отримуємо перші чотири цифри (12345 // 10 -> 1234)
# і беремо останню з них (1234 % 10 -> 4)
digit4 = (number // 10) % 10

# Видобуваємо п'яту цифру (розряд одиниць)
# Беремо залишок від ділення на 10
# Приклад: 12345 % 10 -> 5
digit5 = number % 10

# Збираємо нове число у зворотному порядку
# Кожній цифрі надаємо її новий розряд за допомогою множення
reversed_number = (digit5 * 10000) + (digit4 * 1000) + (digit3 * 100) + (digit2 * 10) + digit1

# Виводимо результат
print("Перевернуте число:", reversed_number)