# Запрашиваем четырехзначное число и преобразуем его в целочисленный тип
number = int(input("Введите четырехзначное число: "))

# Извлекаем первую цифру, используя целочисленное деление
# Пример: 1234 // 1000 = 1
digit1 = number // 1000

# Извлекаем вторую цифру (сотни)
# Сначала получаем первые две цифры: 1234 // 100 = 12.
# Затем берем последнюю цифру от результата: 12 % 10 = 2.
digit2 = (number // 100) % 10

# Извлекаем третью цифру (десятки)
# Сначала получаем первые три цифры: 1234 // 10 = 123.
# Затем берем последнюю цифру от результата: 123 % 10 = 3.
digit3 = (number // 10) % 10

# Извлекаем четвертую цифру (единицы), используя остаток от деления
# Пример: 1234 % 10 = 4
digit4 = number % 10

# Выводим каждую цифру на новой строке
print(digit1)
print(digit2)
print(digit3)
print(digit4)